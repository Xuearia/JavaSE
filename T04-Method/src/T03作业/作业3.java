package T03作业;

import java.util.Scanner;

public class 作业3 {
    public static void main(String[] args) {
//求素数并求和
//题目内容:
//UTC是世界协调时，BJT是北京时间，UTC时间相当于BJT减去8。现在，你的程序要读入一个整数，表示BT的时和分。整数的个位和十位表示
//分，百位和千位表示小时。如果小时小于10，则没有千位部分;如果小时是0，则没有百位部分;如果小时不是0而分小于10分，需要保留十位上
//的0;如果小时是0而分小于10分的，则不需要保留十位上的0。如1124表示11点24分，而905表示9点5分，36表示0点36分，7表示0点7分。
//有效的输入范围是0到2359，即你的程序不可能从测试服务器读到0到2359以外的输入数据。
//你的程序要输出这个时间对应的UTC时间，输出的格式和输入的相同，即输出一个整数，表示UTC的时和分。整数的个位和十位表示分，百位和
//千位表示小时。如果小时小于10，则没有千位部分;如果小时是0，则没有百位部分;如果小时不是0而分小于10分，需要保留十位上的0;如果
//小时是0而分小于10分的，则不需要保留十位上的0。
//提醒:要小心跨日的换算。
//输入格式:
//一个整数，表示BJT的时和分。整数的个位和十位表示分，百位和千位表示小时。如果小时小于10，则没有千位部分;如果小时是0，则没有百位
//部分;如果小时不是0而分小于10分，需要保留十位上的0;如果小时是0而分小于10分的，则不需要保留十位上的0。
//输出格式:
//一个整数，表示UTC的时和分。整数的个位和十位表示分，百位和千位表示小时。如果小时小于10，则没有千位部分;如果小时是0，则没有百位
//部分;如果小时不是0而分小于10分，需要保留十位上的0;如果小时是0而分小于10分的，则不需要保留十位上的0。
//输入样例:
//803
//输出样例:
//3
            Scanner scanner = new Scanner(System.in);
            int bjTime = scanner.nextInt();

            // 提取小时和分钟
            int hour = bjTime / 100; //小时
            int minute = bjTime % 100;//分钟

            // 转换为UTC时间
            int utcHour = hour - 8;//utc时间
            int utcMinute = minute;//utc分钟

            // 处理跨日情况
            if (utcHour < 0) {
                utcHour += 24;
            }

            // 格式化输出
            if (utcHour == 0) {
                System.out.printf("%d%n", utcMinute);
            } else {
                System.out.printf("%02d%02d%n", utcHour, utcMinute);
            }
        }
    }

